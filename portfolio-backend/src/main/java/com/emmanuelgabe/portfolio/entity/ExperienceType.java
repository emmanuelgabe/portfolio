package com.emmanuelgabe.portfolio.entity;

/**
 * Enumeration of experience types for categorizing professional and educational experiences.
 * Used to differentiate between work experiences, education, certifications, and volunteering.
 */
public enum ExperienceType {
    /**
     * Professional work experience (jobs, freelance)
     */
    WORK,

    /**
     * Internship experience (stage)
     */
    STAGE,

    /**
     * Educational experience (degrees, diplomas, courses)
     */
    EDUCATION,

    /**
     * Professional certifications and qualifications
     */
    CERTIFICATION,

    /**
     * Volunteer work and community service
     */
    VOLUNTEERING
}
